package com.evanemran.quickmeal.Models;

public class Ingredient {
    public int id;
    public String name;
    public String localizedName;
    public String image;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getLocalizedName() {
        return localizedName;
    }

    public String getImage() {
        return image;
    }
}
